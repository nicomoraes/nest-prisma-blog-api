export function specialCharacterReplacer(str: string) {
  const accent = [
    'ç',
    'Ç',
    'á',
    'é',
    'í',
    'ó',
    'ú',
    'ý',
    'Á',
    'É',
    'Í',
    'Ó',
    'Ú',
    'Ý',
    'à',
    'è',
    'ì',
    'ò',
    'ù',
    'À',
    'È',
    'Ì',
    'Ò',
    'Ù',
    'ã',
    'õ',
    'ñ',
    'ä',
    'ë',
    'ï',
    'ö',
    'ü',
    'ÿ',
    'Ä',
    'Ë',
    'Ï',
    'Ö',
    'Ü',
    'Ã',
    'Õ',
    'Ñ',
    'â',
    'ê',
    'î',
    'ô',
    'û',
    'Â',
    'Ê',
    'Î',
    'Ô',
    'Û',
  ];
  const withoutAccent = [
    'c',
    'C',
    'a',
    'e',
    'i',
    'o',
    'u',
    'y',
    'A',
    'E',
    'I',
    'O',
    'U',
    'Y',
    'a',
    'e',
    'i',
    'o',
    'u',
    'A',
    'E',
    'I',
    'O',
    'U',
    'a',
    'o',
    'n',
    'a',
    'e',
    'i',
    'o',
    'u',
    'y',
    'A',
    'E',
    'I',
    'O',
    'U',
    'A',
    'O',
    'N',
    'a',
    'e',
    'i',
    'o',
    'u',
    'A',
    'E',
    'I',
    'O',
    'U',
  ];

  for (let i = 0; i < accent.length; i++) {
    str = str.replaceAll(accent[i], withoutAccent[i]);
  }

  return str.replace(/ /g, '-').replace(/[^\w-]+/g, '');
}

